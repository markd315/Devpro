package awesomeSauce;

public class Range {
	public static void main(String[] args) {
		double testNumber = 7.00000000000001;
		if (testNumber > 7 ) {
			System.out.println("greater than 7");
		}
		else if (testNumber < 5) {
			System.out.println("Less than 5");
		}
		else {
			System.out.println("Inside the range of 5-7");
		}
		//
	}
}